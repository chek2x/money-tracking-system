    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addButtonActionPerformed
        double valEx = Double.parseDouble(valueField.getText());
        String item[] = { dateField.getText(), valueExpense(), category(), descriptionArea.getText() };
        MoneyTrackerUI.list.add(item);

        switch (category()) {
            case "Grocery":
                MoneyTrackerUI.groceryTotal += valEx;
                break;
            case "Transportation":
                MoneyTrackerUI.transTotal += valEx;
                break;
            case "Self-Treat":
                MoneyTrackerUI.treatTotal += valEx;
                break;
            case "Property Tax":
                MoneyTrackerUI.taxTotal += valEx;
                break;
            case "Water Bill":
                MoneyTrackerUI.billWTotal += valEx;
                break;
            case "Electricity Bill":
                MoneyTrackerUI.billETotal += valEx;
                break;
            case "Income":
                MoneyTrackerUI.incomeTotal += valEx;
                break;
        }

        MoneyTrackerUI.setTotals();
        MoneyTrackerUI.display();

        dispose();
    }// GEN-LAST:event_addButtonActionPerformed

