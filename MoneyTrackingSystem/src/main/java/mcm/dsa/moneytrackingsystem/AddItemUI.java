    private String valueExpense() {
        if (expenseButton.isSelected()) {
            return "-" + valueField.getText();
        }
        return valueField.getText();

    }

    private String category() {
        if (expenseButton.isSelected()) {
            return categoryBox.getSelectedItem().toString();
        }
        return "Income";
    }

    private void checkBtn() {
        boolean value1 = !dateField.getText().trim().isEmpty();
        boolean value2 = !valueField.getText().trim().isEmpty();
        addButton.setEnabled(value1 == true && value2 == true);
    }

