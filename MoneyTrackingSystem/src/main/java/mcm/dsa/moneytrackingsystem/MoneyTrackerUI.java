        private void jButton_DeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton_DeleteActionPerformed
                int selectedIndex = jTable1.getSelectedRow();
                if (selectedIndex == -1) {
                    String removed = list.get(list.size() - 1)[2];
                    double val = Double.parseDouble(list.get(list.size() - 1)[1]);
                    
                    switch (removed) {
                        case "Grocery":
                            groceryTotal += val;
                            break;
                        case "Transportation":
                            transTotal += val;
                            break;
                        case "Self-Treat":
                            treatTotal += val;
                            break;
                        case "Property Tax":
                            taxTotal += val;
                            break;
                        case "Water Bill":
                            billWTotal += val;
                            break;
                        case "Electricity Bill":
                            billETotal += val;
                            break;
                        case "Income":
                            incomeTotal -= val;
                            break;
                    }
                    list.remove(list.size() - 1);
                } else {
                    String removed = list.get(selectedIndex)[2];
                    double val = Double.parseDouble(list.get(selectedIndex)[1]);
                    
                    switch (removed) {
                        case "Grocery":
                            MoneyTrackerUI.groceryTotal += val;
                            break;
                        case "Transportation":
                            MoneyTrackerUI.transTotal += val;
                            break;
                        case "Self-Treat":
                            MoneyTrackerUI.treatTotal += val;
                            break;
                        case "Property Tax":
                            MoneyTrackerUI.taxTotal += val;
                            break;
                        case "Water Bill":
                            MoneyTrackerUI.billWTotal += val;
                            break;
                        case "Electricity Bill":
                            MoneyTrackerUI.billETotal += val;
                            break;
                        case "Income":
                            MoneyTrackerUI.incomeTotal -= val;
                            break;
                    }
                    list.remove(selectedIndex);
                }
                setTotals();
                display();
        }// GEN-LAST:event_jButton_DeleteActionPerformed

