        public static void sortList() {
                for (int i = 0; i < list.size(); i++) {
                        for (int j = i + 1; j < list.size(); j++) {
                                if (list.get(i)[0].compareTo(list.get(j)[0]) > 0) {
                                        String temp[] = list.get(i);
                                        list.set(i, list.get(j));
                                        list.set(j, temp);
                                }
                        }
                }
        }
        
        public static void setTotals() {
            double[] arr = {groceryTotal, transTotal, treatTotal, taxTotal, billWTotal, billETotal, incomeTotal};
            for (int i = 0; i < totals.length; i++) {
                totals[i] = arr[i];
            }
        }

        public static void display() {
                double sum = 0;
                if (!list.isEmpty()) {
                        jButton_Delete.setEnabled(true);
                        jButton_Summary.setEnabled(true);
                } else {
                        jButton_Delete.setEnabled(false);
                        jButton_Summary.setEnabled(false);
                }

                sortList();

                String[] col = { "Date", "Expense", "Category", "Description" };
                DefaultTableModel model = new DefaultTableModel(col, 0);
                Object[] data = new Object[4];

                for (int i = 0; i < list.size(); i++) {
                        for (int j = 0; j < list.get(i).length; j++) {
                                data[j] = list.get(i)[j];
                        }
                        model.addRow(data);
                }

                jTable1 = new JTable(model);
                jScrollPane2.setViewportView(jTable1);
        }

